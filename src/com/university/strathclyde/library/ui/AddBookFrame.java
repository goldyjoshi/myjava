    private void addToLibraryButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addToLibraryButtonActionPerformed
        String isbn = isbnTextField.getText();
        String bookName = bookNameTextField.getText();
        String authorName = authorNameTextField.getText();
        String bookTitle = bookTitleTextField.getText();
        String bookVersion = bookVersionTextField.getText();
        String bookPrice = bookPriceTextField1.getText();
        String resourceId = bookName.concat("-").concat(isbn);
        PhysicalBook newBook = new PhysicalBook(resourceId, bookName, authorName, bookTitle, isbn);
        newBook.setPrice(Double.valueOf(bookPrice));
        newBook.setVersion(bookVersion);
        LibraryMainUi.getLibrary().addLibraryResourceInLibraryCatalogue(resourceId, newBook);
        Utility.showMessageToUser(this, "Book has been added successfully in the library.");
        this.setVisible(false);
    }//GEN-LAST:event_addToLibraryButtonActionPerformed

