    private void returnjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_returnjButtonActionPerformed
        String damage = damageTextField.getText();
        boolean isDamge = false;
        if (damage != null && !damage.isEmpty()) {
            isDamge = true;
        }
        String resourceId = bookToReturnComboBox.getSelectedItem().toString();
        PhysicalBook physicalBook = (PhysicalBook) LibraryMainUi.getLibrary().getResourceByResourceId(resourceId);
        bookDetailjTextArea.setText(physicalBook.toString());
        memberDetailjTextArea.setText(physicalBook.getIssuedTo().toString());
        String message = LibraryMainUi.getLibrary().returnABook(physicalBook,isDamge,damage);
        Utility.showMessageToUser(this, message);
        this.setVisible(false);
    }//GEN-LAST:event_returnjButtonActionPerformed

