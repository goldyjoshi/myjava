    private void addreourceToLibraryjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addreourceToLibraryjButtonActionPerformed
        String identificationNumber = identificationNumberjTextField.getText();
        String author = authorNmaejTextField.getText();
        String title = resourceTitlejTextField.getText();
        String size = sizeOfResourcejTextField.getText();
        String typeOfResource = tyopeOfResourcejTextField.getText();
        ElectronicDevice selectedElectronicDevice = null;
        String resourceId = String.valueOf(availableDeviceComboBox.getSelectedItem());
        for (ElectronicDevice electronicDevice : LibraryMainUi.getLibrary().getListOfElectronicDevices()) {
            if (resourceId.equals(electronicDevice.getResourceId())) {
                selectedElectronicDevice = electronicDevice;
            }
        }
        ElectronicReadingResource electronicReadingResource = new ElectronicReadingResource(identificationNumber,
                title, author, title, identificationNumber, size, typeOfResource);
        electronicReadingResource.addReadingDevice(selectedElectronicDevice);

            LibraryMainUi.getLibrary().addLibraryResourceInLibraryCatalogue(identificationNumber, electronicReadingResource);
        Utility.showMessageToUser(this, "Electronic reading resource successfully added in library catalogue.");
        this.setVisible(false);
    }//GEN-LAST:event_addreourceToLibraryjButtonActionPerformed

