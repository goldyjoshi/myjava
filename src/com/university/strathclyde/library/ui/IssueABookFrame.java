    private void issueButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_issueButtonActionPerformed
        String selectedResourceId = (String) issueBookComboBox.getSelectedItem();
        PhysicalBook selectedBook = (PhysicalBook) LibraryMainUi.getLibrary().getResourceByResourceId(selectedResourceId);
        String selectedMemberValue = (String) libraryMemberComboBox.getSelectedItem();
        LibraryMember selectedMember = null;
        String memberValue = "";
        for (LibraryMember libraryMember : LibraryMainUi.getLibrary().getLibraryMembers()) {
            memberValue = libraryMember.getFirstName().concat("-").concat(libraryMember.getEmailId());
            if (memberValue.equals(selectedMemberValue)) {
                selectedMember = libraryMember;
            }
        }
        String message = LibraryMainUi.getLibrary().issueBook(selectedBook, selectedMember);
        Utility.showMessageToUser(this, message);
        this.setVisible(false);
    }//GEN-LAST:event_issueButtonActionPerformed

